#ifndef LLVM_LIB_TARGET_Z80old_INSTPRINTER_Z80oldINSTPRINTER_H
#define LLVM_LIB_TARGET_Z80old_INSTPRINTER_Z80oldINSTPRINTER_H

#include "Z80oldInstPrinterBase.h"

namespace llvm {
class Z80oldInstPrinter final : public Z80oldInstPrinterBase {
public:
  Z80oldInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI)
    : Z80oldInstPrinterBase(MAI, MII, MRI) {}

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &OS) override;
  StringRef getRegName(unsigned RegNo) const override {
    return getRegisterName(RegNo);
  }
  static const char *getRegisterName(unsigned RegNo);
};
}

#endif
